public class Praktikum {
	public static void main(String[] args) {
	int number1, number2, results;
	boolean number;
	number1 = 20;
	number2 = 10;
	
	results = number1 / number2; //perintah untuk membagi number1 + number2 yang diambil dari variabel yang dideklarasikan
		System.out.println(results);
	results = number1 + number2; //perintah untuk menambah number1 + number2 yang diambil dari variabel yang dideklarasikan
		System.out.println(results);
	number = (20 != 10);  //perintah untuk membuat pernyataan tidak sama dengan true or false
		System.out.println(number);
	number = (20 == 10); //perintah untuk membuat pernyataan tidak sama dengan true or false
		System.out.println(number);
	}
	
}